    private static class KeyVisitor extends JenkinsfileParserBaseVisitor<ModelASTKey> {
        /**
         * {@inheritDoc}
         */
        @Override
        public ModelASTKey visitKey(JenkinsfileParser.KeyContext ctx) {
            ModelASTKey k = new ModelASTKey(null); // TODO: Need actual source location.
            k.setKey(ctx.getText());

            return k;
        }
    }

    private static class ValueVisitor extends JenkinsfileParserBaseVisitor<ModelASTValue> {
        /**
         * {@inheritDoc}
         */
        @Override
        public ModelASTValue visitSimpleValue(JenkinsfileParser.SimpleValueContext ctx) {
            if (ctx.INT() != null) {
                return ModelASTValue.fromConstant(Integer.valueOf(ctx.INT().getText()), null);
            } else if (ctx.BOOLEAN() != null) {
                return ModelASTValue.fromConstant(Boolean.valueOf(ctx.BOOLEAN().getText()), null);
            } else if (ctx.gstring() != null) {
                return ModelASTValue.fromGString(ctx.gstring().getText(), null);
            } else {
                return ModelASTValue.fromConstant(ctx.STRING().getText(), null);
            }
        }
    }

